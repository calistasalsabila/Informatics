public class Oper_Logical {
    public static void main(String[] args) {
        
        boolean x = true, y = false;

        System.out.println("AND (&&)    : " + (x && y));
        System.out.println("OR (||)     : " + (x || y));
        System.out.println("NOT (!x)    : " + (!x));
    }
    
}
